static const size_t LUT_SZ = sizeof(optimal_rulers)/sizeof(optimal_rulers[0]);

const ruler_t *lut_lookup_by_length(int length)
{
    for (size_t i = 0; i < LUT_SZ; ++i) {
        if (optimal_rulers[i].length == length)
            return &optimal_rulers[i];
    }
    return NULL;
}

void print_ruler(const ruler_t *r)
{
    if (!r) { puts("(null ruler)"); return; }
    printf("length=%d, n=%d, marks=[", r->length, r->marks);
    for (int i = 0; i < r->marks; ++i) {
        printf("%d%s", r->pos[i], (i == r->marks-1) ? "" : ", ");
    }
    puts("]");
}
